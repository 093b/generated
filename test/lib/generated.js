var helper = require('../helper')
var assert = require('assert');

describe('Generated', function() {
  describe('is_generated()', function() {
    it('should detect when files are generated', function() {
      // Generated .NET Docfiles
      assert.ok(helper.is_generated("XML/net_docfile.xml"))
      // Long line
      assert.ok(!helper.is_generated("JavaScript/uglify.js"))

      // Inlined JS, but mostly code
      assert.ok(!helper.is_generated("JavaScript/json2_backbone.js"))

      // Minified JS
      assert.ok(!helper.is_generated("JavaScript/jquery-1.6.1.js"))

      // Go lockfiles
      assert.ok(helper.is_generated("TOML/filenames/Gopkg.lock"))
      assert.ok(helper.is_generated("YAML/filenames/glide.lock"))

      // Cargo generated Cargo.lock file
      assert.ok(helper.is_generated("TOML/filenames/Cargo.lock"))

      // Composer generated composer.lock file
      assert.ok(helper.is_generated("JSON/filenames/composer.lock"))

      // Generated PostScript
      assert.ok(!helper.is_generated("PostScript/sierpinski.ps"))

      // These examples are too basic to tell
      assert.ok(!helper.is_generated("JavaScript/hello.js"))

      assert.ok(!helper.is_generated("JavaScript/intro-old.js"))
      assert.ok(!helper.is_generated("JavaScript/classes-old.js"))

      assert.ok(!helper.is_generated("JavaScript/intro.js"))
      assert.ok(!helper.is_generated("JavaScript/classes.js"))

      assert.ok(helper.is_generated("JavaScript/ccalc-lex.js"))
      assert.ok(helper.is_generated("JavaScript/ccalc-parse.js"))

      // Protocol Buffer generated code
      assert.ok(helper.is_generated("C++/protocol-buffer.pb.h"))
      assert.ok(helper.is_generated("C++/protocol-buffer.pb.cc"))
      assert.ok(helper.is_generated("Java/ProtocolBuffer.java"))
      assert.ok(helper.is_generated("Python/protocol_buffer_pb2.py"))
      assert.ok(helper.is_generated("Go/api.pb.go"))
      assert.ok(helper.is_generated("Go/embedded.go"))
      assert.ok(helper.is_generated("JavaScript/proto.js"))

      // Generated JNI
      assert.ok(helper.is_generated("C/jni_layer.h"))

      // Minified CSS
      assert.ok(!helper.is_generated("CSS/bootstrap.css"))
      assert.ok(helper.is_generated("CSS/bootstrap.min.css"))

      // Generated VCR
      assert.ok(helper.is_generated("YAML/vcr_cassette.yml"))

      // Generated by Zephir
      assert.ok(!helper.is_generated("Zephir/Router.zep"))

      // Go vendored dependencies
      assert.ok(!helper.is_generated("vendor/vendor.json"))
      assert.ok(helper.is_generated("vendor/github.com/kr/s3/sign.go"))
      assert.ok(!helper.is_generated("go/food_vendor/candy.go"))

      // Cython-generated C/C++
      assert.ok(helper.is_generated("C/sgd_fast.c"))
      assert.ok(helper.is_generated("C++/wrapper_inner.cpp"))

      // Unity3D-generated metadata
      assert.ok(helper.is_generated("Unity3D Asset/Tiles.meta"))

      // Racc-generated Ruby
      assert.ok(helper.is_generated("Ruby/racc.rb"))

      // protobuf/grpc-plugin C++
      assert.ok(helper.is_generated("C++/hello.grpc.pb.h"))
      assert.ok(helper.is_generated("C++/grpc.pb.cc"))

      // pkgdown-generateed HTML
      assert.ok(helper.is_generated("HTML/pkgdown.html"))
    });
  });
});

